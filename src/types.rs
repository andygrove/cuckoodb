use std::rc::Rc;

use arrow::array::Array;

/// Value holder for all supported data types
#[derive(Debug, Clone, PartialEq)]
pub enum ScalarValue {
    Boolean(bool),
    Float32(f32),
    Float64(f64),
    Int8(i8),
    Int16(i16),
    Int32(i32),
    Int64(i64),
    UInt8(u8),
    UInt16(u16),
    UInt32(u32),
    UInt64(u64),
    Utf8(String),
    Struct(Vec<ScalarValue>),
}

pub enum Value {
    Column(Rc<Array>),
    Scalar(Rc<ScalarValue>),
}
